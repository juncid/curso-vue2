<template>
  <ul class="list-group tasks">
    <app-task v-for="(task, index) in tasks" :key="task.id"
              :tasks="tasks" :task="task" :index="index" @remove="deleteTask">
    </app-task>
  </ul>
</template>

<script>
    import Task from '../Tasks/Tasks'

    export default {
        components: {
          'app-task': Task
        },
        props: ['tasks'],
        methods: {
          deleteTask(index) {
            this.tasks.splice(index, 1);
          },
        }
    };
</script>
